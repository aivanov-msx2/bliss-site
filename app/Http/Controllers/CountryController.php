    /**
     * Display the specified resource.
     *
     * @param  \App\Models\Country  $country
     * @return \Illuminate\Http\Response
     */
    public function show(Country $country)
    {
        
        try {
    
            $wineriesData = Winery::getWineriesByCountry($country);
            $countryData = ['name' => $country->name];
            
            $data = [
                'country' => $countryData,
                'wineries' => $wineriesData,
                'country_image_file' => ($country->country_image_file) ? Country::getCountryImagePublicLocation() . "/" . $country->country_image_file : "",
            ];

            return $this->respond($data);

        } catch (\Exception $e) {

            return $this->respondInternalError('API Internal Error.', $e);

        }
    }

